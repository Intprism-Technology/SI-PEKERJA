        $nodes = NodesReport::get()->unique('node_id')->count();
        $nodesConnected = NodesReport::where('created_at', '>=', Carbon::now()->subMinutes(5))->get()->unique('node_id')->count();
        $alertReport = new Alert;
        return view('dash.index', compact(
            'alerts',
            'status',
            'nodes',
            'nodesConnected',
            'alertReport'
        ));
    }

